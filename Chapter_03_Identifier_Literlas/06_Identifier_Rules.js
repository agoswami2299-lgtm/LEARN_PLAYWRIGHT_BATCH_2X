/* Identifier Rules:
1. Must not be a reserved keyword  
   let function = 1; ❌
2. Can contain letters, digits, underscores, and dollar signs  
   user_name$1
3. Must start with a letter, underscore, or dollar sign (not a digit)  
   1name ❌
4. Case-sensitive  
   Name and name are different
5. No spaces allowed  
   user name ❌
6. Unicode letters are allowed  
   用户
7. Cannot contain special characters except $ and _  
   user-name ❌*/